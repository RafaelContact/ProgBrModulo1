//  % módulo
//  ++ incremento
//  -- Decremento

var a= 30;

var b = a--;
//  nesse caso ele coloca o valor de a em b e depois decrescenta de a, então b já recebeu a e não exibe seu valor alterado
console.log(b);
console.log(a);

var c = --a;

console.log(c);
// nesse caso ele decresce 1 e depois acrescenta o valor de a

--a; // diminui um
++a; // decrescenta um